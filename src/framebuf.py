    def text(self, s, x, y, c=1):
        cx = x
        for ch in s:
            code = ord(ch)
            glyph = self._FONT.get(code, self._FONT.get(63))  # fallback to '?'
            for row_idx, row_byte in enumerate(glyph):
                for bit in range(8):
                    # Font bytes are stored LSB-first (bit 0 = leftmost pixel)
                    if row_byte & (1 << bit):
                        self.pixel(cx + bit, y + row_idx, c)
                    else:
                        self.pixel(cx + bit, y + row_idx, 0 if c else 1)
            cx += 8

    # ── blit ─────────────────────────────────────────────────────────────

    def blit(self, src, x, y, key=-1):
        """Blit another FrameBuffer onto this one.
        key: colour treated as transparent (-1 = none, 0 = black, 1 = white)
        """
        for row in range(src.height):
            for col in range(src.width):
                v = src.pixel(col, row)
                if key != -1 and v == key:
                    continue
                self.pixel(x + col, y + row, v)
