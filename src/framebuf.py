"""
framebuf.py  —  Pure-Python drop-in for MicroPython's framebuf module.
Implements only MONO_HLSB (the only format catode32 uses).
"""

MONO_HLSB = 3   # same constant value as MicroPython

class FrameBuffer:
    """
    Minimal MONO_HLSB FrameBuffer.
    Each row is stored as ceil(width/8) bytes, MSB-first (H = horizontal,
    L = LSB-last means MSB is drawn first → bit 7 of byte 0 is pixel (0,0)).
    """

    def __init__(self, buf, width, height, fmt, stride=None):
        if fmt != MONO_HLSB:
            raise ValueError("Only MONO_HLSB supported in this stub")
        self._buf    = buf
        self.width   = width
        self.height  = height
        self._stride = stride if stride is not None else width  # in pixels

    # ── internal helpers ─────────────────────────────────────────────────

    def _byte_bit(self, x, y):
        """Return (byte_index, bit_mask) for pixel (x, y)."""
        row_bytes = (self._stride + 7) // 8
        byte_idx  = y * row_bytes + x // 8
        bit_mask  = 0x80 >> (x & 7)
        return byte_idx, bit_mask

    # ── pixel ─────────────────────────────────────────────────────────────

    def pixel(self, x, y, c=None):
        if x < 0 or x >= self.width or y < 0 or y >= self.height:
            if c is None:
                return 0
            return
        idx, mask = self._byte_bit(x, y)
        if c is None:                       # read
            return 1 if (self._buf[idx] & mask) else 0
        if c:                               # write 1
            self._buf[idx] |= mask
        else:                               # write 0
            self._buf[idx] &= ~mask & 0xFF

    # ── fill ─────────────────────────────────────────────────────────────

    def fill(self, c):
        v = 0xFF if c else 0x00
        for i in range(len(self._buf)):
            self._buf[i] = v

    def fill_rect(self, x, y, w, h, c):
        for row in range(y, y + h):
            for col in range(x, x + w):
                self.pixel(col, row, c)

    # ── shapes ───────────────────────────────────────────────────────────

    def rect(self, x, y, w, h, c):
        self.hline(x,         y,         w, c)
        self.hline(x,         y + h - 1, w, c)
        self.vline(x,         y,         h, c)
        self.vline(x + w - 1, y,         h, c)

    def hline(self, x, y, w, c):
        for col in range(x, x + w):
            self.pixel(col, y, c)

    def vline(self, x, y, h, c):
        for row in range(y, y + h):
            self.pixel(x, row, c)

    def line(self, x1, y1, x2, y2, c):
        dx, dy = abs(x2 - x1), abs(y2 - y1)
        sx = 1 if x1 < x2 else -1
        sy = 1 if y1 < y2 else -1
        err = dx - dy
        while True:
            self.pixel(x1, y1, c)
            if x1 == x2 and y1 == y2:
                break
            e2 = 2 * err
            if e2 > -dy:
                err -= dy
                x1  += sx
            if e2 < dx:
                err += dx
                y1  += sy

